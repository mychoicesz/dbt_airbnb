SELECT
STATEFIPS, STATE, ZIPCODE, 
A07240 as RETIREMENT_SAVING_CONTRIBUTE,
N07240 AS NUM_OF_RETURNS_WITH_RETIRE_SAVING_CONTR,
A04800 AS TAXABLE_INCOME,
N04800 AS NUM_OF_RETURNS_WITH_TAXABLE_INCOME,
A00200 AS SALARIES_WAGES,
A02650 AS TOTAL_INCOME,
N02650 AS NUM_OF_RETURNS_WITH_TOT_INCOME,
A00100 AS AGI,
AGI_STUB AS AGI_CLASS,
REGEXP_SUBSTR(FILE_NAME, '\\d{4}') AS INC_YEAR
FROM
TAX_LANDING_ZONE